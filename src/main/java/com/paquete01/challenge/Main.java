    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int n = 0;
        int cnt = 0;
        String lineRead = "";

        try {
            File window = new File(System.getProperty("user.home"));
            JFileChooser choice = new JFileChooser();
            choice.setCurrentDirectory(window);
            int option = choice.showOpenDialog(this);
            File selectedFile = choice.getSelectedFile();
            File numeros = new File(selectedFile.getAbsolutePath());

            try (LineNumberReader reader = new LineNumberReader(new FileReader(selectedFile))) {
                while ((lineRead = reader.readLine()) != null) {}
                cnt = reader.getLineNumber();
            }
            integers = new int[cnt];
            Scanner in = new Scanner(numeros);
            
            while (in.hasNext()) {
                String data = in.nextLine();
                integers[i++] = parseInt(data);
                
            }
            System.out.println("bulk file loaded correctly");
        } 
        catch (FileNotFoundException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

